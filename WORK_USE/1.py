a = list(range(2, 21)) #Список послідовних цілих чисел від 2 до 19 включно
print(a)
b = [x**2 for x in range(1, 11)] #Список квадратів послідовних цілих чисел від 1 до 10 включно
print(b)
c = [x**2 for x in range(10, -1, -1)] #Список квадратів послідовних цілих чисел від 10 до 0 включно
print(c)
d = [x for x in range(101) if x % 2 != 0 and x % 3 != 0 and x % 5 != 0] #Список цілих чисел від 0 до 100 включно, які не діляться ні на одно з чисел 2, 3, 5
print(d)

